package Genome::Model::Command::ImportedAnnotation::CopyRibosomalGeneNames;

use strict;
use warnings;
use Genome;

class Genome::Model::Command::ImportedAnnotation::CopyRibosomalGeneNames {
    is => 'Command::V2',
    has => [
        species_name => {
            is => 'Text',
            is_input => 1,
            valid_values => ['human', 'mouse'],
            default => 'human',
        },
        output_file => {
            is => 'FilePath',
            doc => 'Path to output the RibosomalGeneNames to',
        }
    ],
};

sub execute {
    my $self = shift;
    my $output_fh = Genome::Sys->open_file_for_writing($self->output_file);
    my $species_name = $self->species_name;
    my $method_name = join('_', 'get', $species_name, 'ribosomal_gene_names');
    $output_fh->print($self->$method_name);
    $output_fh->close;
    return 1;
}

