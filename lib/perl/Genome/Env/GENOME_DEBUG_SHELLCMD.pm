package Genome::Env::GENOME_DEBUG_SHELLCMD;
our $default_value = undef;

=pod

=head1 NAME

GENOME_DEBUG_SHELLCMD

=head1 DESCRIPTION

The GENOME_DEBUG_SHELLCMD environment is a regex which, when it matches a shell command text, will stop before running that command.

=head1 DEFAULT VALUE

 /var/lib/genome/db/

=cut

1;
